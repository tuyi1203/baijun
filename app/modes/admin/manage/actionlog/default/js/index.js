$(function()
{
   $('#print').click(function(){
	   $("#listForm").attr('action','manage_bookinglog-print-default.html');
	   $("#listForm").attr('target','_blank');
	   document.forms[0].submit();
	   $("#listForm").attr('action', 'manage_bookinglog-default-list.html');
	   $("#listForm").attr('target' , '_self');
   });
});